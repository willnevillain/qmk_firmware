bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case DFLT:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_DFLT);
      }
      return false;
      break;
    case LWR:
      if (record->event.pressed) {
        layer_on(_LWR);
        update_tri_layer(_LWR, _RSE, _GME);
      } else {
        layer_off(_LWR);
        update_tri_layer(_LWR, _RSE, _GME);
      }
      return false;
      break;
    case RSE:
      if (record->event.pressed) {
        layer_on(_RSE);
        update_tri_layer(_LWR, _RSE, _GME);
      } else {
        layer_off(_RSE);
        update_tri_layer(_LWR, _RSE, _GME);
      }
      return false;
      break;
  }
  return true;
}

void encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if (clockwise) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    }
    else if (index == 1) {
        if (clockwise) {
            tap_code(KC_PGDN);
        } else {
            tap_code(KC_PGUP);
        }
    }
}
